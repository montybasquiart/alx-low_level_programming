#include <stdio.h>

/**
 * addition - adds two integers
 * @a: first paremeter
 * @b: second parameter
 * Return: adds final result
 */
int add(int a, int b)
{
  return (a + b);
}

/**
 * division - divides two integers
 * @a: first paremeter
 * @b: second parameter
 * Return: divides final number
 */
int div(int a, int b)
{
  return (a / b);
}

/**
 * modulus - mod of two integers
 * @a: first paremeter
 * @b: second parameter
 * Return: checks absolute value of final number
 */
int mod(int a, int b)
{
  return (a % b);
}

/**
 * multiplication - multiply two integers
 * @a: first paremeter
 * @b: second parameter
 * Return: multiplies final number
 */
int mul(int a, int b)
{
  return (a * b);
}

/**
 * subtract - subtract two integers
 * @a: first paremeter
 * @b: second parameter
 * Return: subtract final result
 */
int sub(int a, int b)
{
  return (a - b);
}
